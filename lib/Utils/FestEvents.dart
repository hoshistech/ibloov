class FestEvents {
  var start;
  var year;
  var month;
  var date;
  var day;
  var time;
  var address1;
  var address2;
  var rate;
  var id;

  FestEvents(id, start, year, month, date, day, time, address1, address2, rate) {
    this.id = id;
    this.start = start;
    this.year = year;
    this.month = month;
    this.date = date;
    this.day = day;
    this.time = time;
    this.address1 = address1;
    this.address2 = address2;
    this.rate = rate;
  }
}
